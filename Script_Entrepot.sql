/* SCRIPT DE CREATION DES TABLES */
/* Les tables ne possédant pas de clés étrangères sont créées en premier dans la base de données */

/* Création de la table GAMME */
CREATE TABLE GAMME
(
CODEG VARCHAR2(5) NOT NULL,
NOMG VARCHAR2(50) NOT NULL,
SECTEURG VARCHAR2(50) NOT NULL
CHECK(SECTEURG IN ('Gastronomie', 'Avantages salariés', 'Loisirs et culture','Vacances et voyage')),
CONSTRAINT PK_GAMME PRIMARY KEY (CODEG)
);

/* Création de la table CATEGORIE */
CREATE TABLE CATEGORIE
(
CODECAT VARCHAR2(5) NOT NULL,
NOMCAT VARCHAR2(100) NOT NULL,
SUPERCAT VARCHAR2(100) NOT NULL,
CONSTRAINT PK_CATEGORIE PRIMARY KEY (CODECAT)
);

/* Les tables possédant des clés étrangères sont maintenant crées dans la base de données */
/* Création de la table PRODUIT */
CREATE TABLE PRODUIT
(
CODEP NUMBER(5) NOT NULL,
NOMP VARCHAR2(50),
MARQUEP VARCHAR2(50),
PRIX_LOC_JOUR NUMBER(5,2)
CHECK (PRIX_LOC_JOUR>0),
PRIX_VENTE NUMBER(5,2)
CHECK (PRIX_VENTE>0),
CODEG VARCHAR2(5),
CONSTRAINT PK_PRODUIT PRIMARY KEY (CODEP)
);

/* Création de la table EXEMPLAIRE */
CREATE TABLE EXEMPLAIRE
(
CODEEX NUMBER(5) NOT NULL,
CODEP NUMBER(5) NOT NULL,
CONSTRAINT PK_EXEMPLAIRE PRIMARY KEY (CODEEX)
);

/* Création de la table DOMAINE */
CREATE TABLE DOMAINE
(
CODEDO VARCHAR2(7) NOT NULL,
NOMDO VARCHAR2(100) NOT NULL,
CODEDO_SUP VARCHAR2(7),
CONSTRAINT PK_DOMAINE PRIMARY KEY (CODEDO)
);

/* Création de la table SOCIETE */
CREATE TABLE SOCIETE
(
CODESOC VARCHAR2(14) NOT NULL,
NOMSOC VARCHAR2(100),
VILLESOC VARCHAR2(50),
DEPARTEMENTSOC VARCHAR2(50),
REGIONSOC VARCHAR2(50),
CODEDO VARCHAR2(7) NOT NULL,
CONSTRAINT PK_SOCIETE PRIMARY KEY (CODESOC)
);

/* Création de la table SERVICE */
CREATE TABLE SERVICE
(
CODESE NUMBER(4) NOT NULL,
NOMSE VARCHAR2(50),
CODESOC VARCHAR2(14) NOT NULL,
CONSTRAINT PK_SERVICE PRIMARY KEY (CODESE)
);

/* Création de la table EMPLOYE */
CREATE TABLE EMPLOYE
(
CODEE NUMBER(4) NOT NULL,
NOME VARCHAR2(50),
PRENOME VARCHAR2(50),
CODECAT VARCHAR2(5) NOT NULL,
CODESE NUMBER(4) NOT NULL,
CONSTRAINT PK_EMPLOYE PRIMARY KEY (CODEE)
);

/* Création de la table REDUCTION */
CREATE TABLE REDUCTION
(
CODEG VARCHAR2(5) NOT NULL,
CODECAT VARCHAR2(5) NOT NULL,
ANNEE NUMBER(5) NOT NULL
CHECK (ANNEE>0),
REDUC_LOC NUMBER(5,3)
CHECK (REDUC_LOC>0 AND REDUC_LOC<=1),
REDUC_ACHAT NUMBER(5,3)
CHECK (REDUC_ACHAT>0 AND REDUC_ACHAT<=1),
CONSTRAINT PK_REDUCTION PRIMARY KEY (CODEG,CODECAT,ANNEE)
);

/* Création de la table ACHAT */
CREATE TABLE ACHAT
(
CODEP NUMBER(5) NOT NULL,
CODEE NUMBER(4) NOT NULL,
DATEA DATE NOT NULL,
QTE NUMBER(5) NOT NULL
CHECK (QTE>0),
CONSTRAINT PK_ACHAT PRIMARY KEY (CODEP,CODEE,DATEA)
);

/* Création de la table RESERVER */
CREATE TABLE RESERVER
(
CODEP NUMBER(5) NOT NULL,
CODEE NUMBER(4) NOT NULL,
DATELPREV DATE,
NB_EX_PREV NUMBER(4) NOT NULL
CHECK (NB_EX_PREV>0),
DATERESA DATE NOT NULL,
DUREE_PREV NUMBER(4) NOT NULL
CHECK (DUREE_PREV>0),
CONSTRAINT PK_RESERVER PRIMARY KEY (CODEP,CODEE,DATELPREV)
);

/* Création de la table LOUER */
CREATE TABLE LOUER
(
CODEEX NUMBER(5) NOT NULL,
CODEE NUMBER(4) NOT NULL,
DATEL DATE NOT NULL,
DUREE_LOC NUMBER(4) NOT NULL
CHECK (DUREE_LOC>0),
CONSTRAINT PK_LOUER PRIMARY KEY (CODEEX,CODEE,DATEL)
);

