/* Gestion des dates */
create or replace TRIGGER CHECK_DATE_ACHAT
BEFORE INSERT ON ACHAT
REFERENCING NEW AS N 
FOR EACH ROW
BEGIN
  IF(:N.DATEA>SYSDATE) then
    raise_application_error(-20100, 'La date d''achat saisie n''est pas valide');
  END IF;
END;
/
create or replace TRIGGER CHECK_DATE_LOCATION
BEFORE INSERT ON LOUER
REFERENCING NEW AS N 
FOR EACH ROW
BEGIN
  IF(:N.DATEL>SYSDATE) then
    raise_application_error(-20101, 'La date de location saisie n''est pas valide');
  END IF;
END;
/
create or replace TRIGGER CHECK_DATE_RESERVATION
BEFORE INSERT ON RESERVER
REFERENCING NEW AS N 
FOR EACH ROW
BEGIN
  IF(:N.DATELPREV>=SYSDATE) then
    raise_application_error(-20102, 'La date de reservation précédente saisie n''est pas valide');
  END IF;
END;
/
create or replace TRIGGER NOUVEL_EMPLOYE
BEFORE INSERT ON EMPLOYE
REFERENCING NEW AS N 
FOR EACH ROW
BEGIN
  :N.CODEE:=SEQ_EMPLOYE.NEXTVAL();
END;
/
create or replace TRIGGER NOUVEAU_SERVICE
BEFORE INSERT ON SERVICE
REFERENCING NEW AS N 
FOR EACH ROW
BEGIN
  :N.CODESE:=SEQ_SERVICE.NEXTVAL();
END;
/
create or replace TRIGGER NOUVEL_EXEMPLAIRE
BEFORE INSERT ON EXEMPLAIRE
REFERENCING NEW AS N 
FOR EACH ROW
BEGIN
  :N.CODEEX:=SEQ_EXEMPLAIRE.NEXTVAL();
END;
/
create or replace TRIGGER NOUVEAU_PRODUIT
BEFORE INSERT ON PRODUIT
REFERENCING NEW AS N 
FOR EACH ROW
BEGIN
  :N.CODEP:=SEQ_PRODUIT.NEXTVAL();
END;
